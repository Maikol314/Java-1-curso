package unidad3;

public class Actividad_13 {

	public static void main(String[] args) {
		
		int numero = 7, multiplicador = 1;
		
		while (multiplicador < 11) {
			
			int resultado = numero * multiplicador;
			
			System.out.println(numero + " x " + multiplicador + " es: " + resultado);
			
			multiplicador += 1;
		}

	}

}
